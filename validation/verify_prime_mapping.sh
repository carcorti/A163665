#!/usr/bin/env bash
set -euo pipefail

if ! command -v primecount >/dev/null 2>&1; then
  echo "primecount not found" >&2
  exit 127
fi

ok=1

while read -r n k; do
  got=$(primecount "$n" --nth-prime --threads=1)
  if [[ "$got" == "$k" ]]; then
    printf '%s\t%s\t%s\tOK\n' "$n" "$k" "$got"
  else
    printf '%s\t%s\t%s\tFAIL\n' "$n" "$k" "$got"
    ok=0
  fi
done <<'EOF'
